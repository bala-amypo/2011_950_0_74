
public class student{
    private int id;
    private String name;
    private String email;
}
public int getId(){
    return id;
}
public void setId(int id){
    this.id=id;
}
public String getname(){
    return name;
}
public void setname(String name){
    this.name=name;
}
public String getemail(){
    return email;
}
public void setemail(String email){
    this.email=email;
}